<script lang="ts">
	import { fade } from 'svelte/transition';
	import { Recorder } from './recorder';

	interface Props {
		video: HTMLVideoElement | null;
	}

	let { video }: Props = $props();

	let recorder: Recorder | null = $derived.by(() => {
		if (!video) {
			return null;
		}
		const stream = (
			video as HTMLVideoElement & { captureStream: () => MediaStream }
		).captureStream();
		return new Recorder(stream);
	});

	let recording = $state(false);
</script>

{#if recorder}
	<recording-button>
		<button
			onclick={async () => {
				recording = !recording;
				if (!recorder) {
					return;
				}
				if (recording) {
					recorder.start();
				} else {
					const blob = await recorder.stop();
					const link = document.createElement('a');
					link.href = URL.createObjectURL(blob);
					link.download = 'recording.webm';
					link.click();
				}
			}}
			aria-pressed={recording}
		>
			{#if recording}
				<img in:fade src="/recording.svg" alt="Stop Recording" />
			{:else}
				<img in:fade src="/record.svg" alt="Record" />
			{/if}
		</button>
	</recording-button>
{/if}

<style>
	recording-button button {
		background: none;
		border: none;
		cursor: pointer;
		border-radius: 100%;
	}

	@keyframes spin {
		from {
			transform: rotate(0deg);
		}
		to {
			transform: rotate(360deg);
		}
	}

	recording-button button img {
		animation: spin 2s linear infinite; /* rotates once every 2s */
		width: 40px;
	}
</style>
